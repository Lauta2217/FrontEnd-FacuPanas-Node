drop table Materia;
drop table Carrera;
drop table Archivo;

create table Carrera(
	CodPropuesta varchar(50) primary key,
	Nombre varchar(100) not null
);

create table Materia(
	CodMateria varchar(50),
	Nombre varchar(100) not null,
	Anio integer not null,
	CodPropuesta varchar(50) not null,
	primary key(CodMateria,CodPropuesta),
	foreign key (CodPropuesta) references Carrera(CodPropuesta)
);

create table Archivo(
	Idd integer generated by default as identity,
	CodMateria varchar(50),
	CodPropuesta varchar(50),
	Url text not null,
	unidad integer not null,
	nombre_archivo varchar(50) not null,
	tipo varchar(50) not null check (tipo in ('teoria','practica','resumen','ejercicio','ayuda')),
	primary key (Idd,CodMateria),
	foreign key (CodMateria, CodPropuesta) references Materia(CodMateria, CodPropuesta)
);

